package gavin.sensual.app.setting;

/**
 * 版本信息
 *
 * @author gavin.xiong 2017/8/8
 */
public class Version {

    private int code;
    private String name;
    private String msg;
    private String url;

    public int getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public String getMsg() {
        return msg;
    }

    public String getUrl() {
        return url;
    }
}
